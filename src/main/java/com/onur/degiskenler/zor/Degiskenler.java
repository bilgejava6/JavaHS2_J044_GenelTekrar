package com.onur.degiskenler.zor;

public class Degiskenler {

    private int sayi;
    private float ondalik;
    private double ondalik2;
    private char karakter;

    public int getSayi() {
        return sayi;
    }
    public void setSayi(int sayi) {
        this.sayi = sayi;
    }
    public float getOndalik() {
        return ondalik;
    }
    public void setOndalik(float ondalik) {
        this.ondalik = ondalik;
    }
    public double getOndalik2() {
        return ondalik2;
    }
    public void setOndalik2(double ondalik2) {
        this.ondalik2 = ondalik2;
    }
    public char getKarakter() {
        return karakter;
    }
    public void setKarakter(char karakter) {
        this.karakter = karakter;
    }
}
