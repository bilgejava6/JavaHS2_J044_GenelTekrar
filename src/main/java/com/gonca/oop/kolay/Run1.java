package com.gonca.oop.kolay;

public class Run1 {
    //Bir class oluşturun ve bu sınıfta bir özellik (field) ve bir metot tanımlayın.
    private static int ses;

    public Run1() {
        this.ses = ses;
    }

    public int getSes() {
        return ses;
    }

    public void setSes(int ses) {
        this.ses = ses;
    }

    public static void arttir()
    {
        ses++;
    }

}
